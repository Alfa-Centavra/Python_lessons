print('Задача 1. Герон')
import math

a = int(input('1-я сторона треугольника: '))
b = int(input('2-я сторона треугольника: '))
c = int(input('3-я сторона треугольника: '))

p = (a + b + c) / 2 #полупериметр треугольника

s = round(math.sqrt(p * (p - a) * (p - b) * (p - c)), 3)

print('площадь треугольника, сторонами', a, ',', b, ',', c, '=', s)